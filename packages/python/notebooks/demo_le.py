@app.cell
def _(X_scaled, cache_dir, marker_names, phenotypes):
    import dtour as _dtour2

    fisher_tour_path = cache_dir / "le_tour_fisher.npz"
    if fisher_tour_path.exists():
        fisher_tour = _dtour2.TourResult.load(fisher_tour_path)
    else:
        fisher_tour = _dtour2.le_tour(
            X_scaled,
            n_frames=8,
            n_neighbors=15,
            feature_names=marker_names,
            random_state=42,
            labels=phenotypes.to_numpy(),
            discriminative=True,
        )
        fisher_tour.save(fisher_tour_path)
    return (fisher_tour,)


@app.cell
def _(dtour, fisher_tour, phenotype_colors, phenotypes, pl):
    fisher_df = pl.DataFrame(
        {f"FD{i}": fisher_tour.embedding[:, i] for i in range(fisher_tour.embedding.shape[1])}
    ).with_columns(phenotypes)

    fisher_w = dtour.Widget(
        data=fisher_df,
        tour=fisher_tour,
        preview_count=min(8, fisher_tour.n_views),
        preview_size="small",
        point_color="phenotypes",
        color_map=phenotype_colors,
        camera_zoom=0.5,
        height=900,
        theme="light",
    )
    fisher_w
    return (fisher_df,)


@app.cell
def _(cache_dir, dtour, fisher_df, fisher_tour, phenotype_colors):
    fisher_json = dtour.build_dtour_metadata(
        point_color="phenotypes",
        tour_by="dimensions",
        preview_count=min(8, fisher_tour.n_views),
        preview_scale=0.5,
        camera_zoom=0.5,
        color_map=phenotype_colors,
        tour=fisher_tour,
    )
    fisher_out_path = cache_dir / "mair-2022-tumor-le-fisher.pq"
    fisher_df.write_parquet(
        str(fisher_out_path),
        compression="zstd",
        compression_level=9,
        metadata={"dtour": fisher_json},
    )
    fisher_out_path
    return


