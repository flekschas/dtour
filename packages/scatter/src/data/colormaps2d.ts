/**
 * 2D Colormap LUT data (SVD rank-1 decomposition of pycolormap-2d reference images).
 *
 * Each colormap is decomposed into 3 channels (R, G, B). Each channel is
 * approximated as a rank-1 outer product: channel(x,y) ≈ x_curve[x] * y_curve[y].
 * Ziegler B channel uses a rank-2 correction for accuracy.
 *
 * Values are normalized so that x_curve[i] * y_curve[j] ≈ channel value in [0, 1].
 *
 * Buffer layout (128 f32 = 512 bytes):
 *   R_X[16] R_Y[16] G_X[16] G_Y[16] B_X[16] B_Y[16] B_X2[16] B_Y2[16]
 * For rank-1 colormaps, B_X2 and B_Y2 are all zeros.
 */

