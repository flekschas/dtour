type LutData = {
  r_x: number[];
  r_y: number[];
  g_x: number[];
  g_y: number[];
  b_x: number[];
  b_y: number[];
  b_x2?: number[];
  b_y2?: number[];
};

const CMAPS: LutData[] = [SCHUMANN, BREMM, STEIGER, ZIEGLER, TEULINGFIG2, CUBEDIAGONAL];

/**
 * Pack a colormap's SVD curves into a Float32Array for GPU upload.
 * Layout: R_X[16] R_Y[16] G_X[16] G_Y[16] B_X[16] B_Y[16] B_X2[16] B_Y2[16]
 * Returns 128 f32 values (512 bytes).
 */
export const packColormap2DLut = (mapIndex: number): Float32Array | null => {
  if (mapIndex >= CMAPS.length) return null; // procedural (oklab_polar)
  const cm = CMAPS[mapIndex]!;
  const buf = new Float32Array(128);
  buf.set(cm.r_x, 0);
  buf.set(cm.r_y, 16);
  buf.set(cm.g_x, 32);
  buf.set(cm.g_y, 48);
  buf.set(cm.b_x, 64);
  buf.set(cm.b_y, 80);
  buf.set(cm.b_x2 ?? ZEROS_16, 96);
  buf.set(cm.b_y2 ?? ZEROS_16, 112);
  return buf;
};
